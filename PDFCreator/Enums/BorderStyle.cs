namespace PDFCreator.Enums
{
    public enum BorderStyle
    {
        // No border.
        None = 0,
        // A single solid line.
        Single = 1,
        // A dotted line.
        Dot = 2,
        // A dashed line (small gaps).
        DashSmallGap = 3,
        // A dashed line (large gaps).
        DashLargeGap = 4,
        // Alternating dashes and dots.
        DashDot = 5,
        // A dash followed by two dots.
        DashDotDot = 6
    }
}
